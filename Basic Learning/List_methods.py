#Lists methods
list = [2,6,37,8,4,9,8,7]

#append , adding 1 element at end
list.append(23)
print(list)

#sort, in asc order
list.sort()
list.sort(reverse = True)
print(list)

#reverse
list.reverse()
print(list)

#insert
list.insert(3,98)
print(list)

#remove
list.remove(2)
print(list)

#pop
list.pop(5)
print(list)